void draw_vmu_loading() {
    static float vmuUpdate = 0;
    static int vmucounter = 0;
    static int vmuframe = 0;



    maple_device_t *vmu = maple_enum_type(0, MAPLE_FUNC_MEMCARD);

    if (!vmu) {
        return;
    }

    float now = GetTime();
    if (now - vmuUpdate < 0.20f) return;
    vmuframe++;
    if (vmuframe >= 4){
        vmuframe = 0;
    }
    vmuUpdate = now;

 
    switch (vmuframe) {
        case 0:
        vmufb_paint_area(&vmu_fb, 0, 0, 48, 32, VMU_Save_1);
        vmufb_present(&vmu_fb, vmu);
        break;
        case 1:
        vmufb_paint_area(&vmu_fb, 0, 0, 48, 32, VMU_Save_2);
        vmufb_present(&vmu_fb, vmu);
        break;
        case 2:
        vmufb_paint_area(&vmu_fb, 0, 0, 48, 32, VMU_Save_3);
        vmufb_present(&vmu_fb, vmu);
        break;
        case 3:
        vmufb_paint_area(&vmu_fb, 0, 0, 48, 32, VMU_Save_4);
        vmufb_present(&vmu_fb, vmu);
        break;

    }
    
        
        
    
}
