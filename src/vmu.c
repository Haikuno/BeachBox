vmufb_t vmu_fb;




void draw_vmu_animation() {
    static float vmuUpdate = 0;
    static int vmuframe = 0;
    maple_device_t *vmu = maple_enum_type(0, MAPLE_FUNC_MEMCARD);
    
    if (!vmu) {
        return;
    }
    
    float now = GetTime();
    if (now - vmuUpdate < 0.20f) return;
    
    vmuUpdate = now;

    switch (current_scene)
    {
    case RAYLOGO:
        break;
    case LOADING:
        vmuframe = (vmuframe + 1) % 4; //Cypress: 4 is number of frames, this resets it's self with
        // modulo operator
        vmufb_paint_area(&vmu_fb, 0, 0, 48, 32, VMU_Frames_Save[vmuframe]);
        break;
    case MAINMENU:
        break;
    case GAME:
        
        if (is_game_over){
            vmuframe = (vmuframe + 1) % 2;
            vmufb_paint_area(&vmu_fb, 0, 0, 48, 32, VMU_Frames_GameOver[vmuframe]);
        } else {
            vmuframe = (vmuframe + 1) % 5;
            vmufb_paint_area(&vmu_fb, 0, 0, 48, 32, VMU_Frames_Game[vmuframe]);
        }
        
        break;
    case SHOP:
        break;
    case OPTIONS:
        break;
    case CREDITS:
        break;
    }
   
    
    vmufb_present(&vmu_fb, vmu);
};
  